    public boolean isAvailable(int y, int x) {
        return gameMap[y][x] == 0;
    }

    public boolean isWall(int y, int x) {
        return gameMap[y][x] == 1;
    }

    public int width() {
        return gameMap.length;
    }

    public int height() {
        return gameMap[0].length;
    }

    public boolean isAvailable(Position pos) {
        return isAvailable(pos.getY(), pos.getX());
    }

    public int[][] getGameMap() {
        return gameMap;
    }

    @Override
    public void renderObject(Graphics graphics, Level level) throws IOException {
        BufferedImage wallImg = ImageIO.read(new FileInputStream("src/img/wall.png"));
        for (int y = 0; y < width(); y++) {
            for (int x = 0; x < height(); x++)
                if (isWall(y, x))
                    graphics.drawImage(wallImg, distanceBetweenHeadingX + x * hitboxSize, distanceBetweenHeadingY + y * hitboxSize, wallImgX, wallImgY, null);
        }
    }
